// --- Функция переключения ---

let currentLang = 'en';

function switchLanguage(lang) {
  currentLang = lang;
  const t = translations[lang];

  // Навигация
  document.querySelector('a[href="#projects"]').textContent = t.navProjects;
  document.querySelector('a[href="#skills"]').textContent = t.navSkills;
  document.querySelector('a[href="#about"]').textContent = t.navAbout;
  document.querySelector('a[href="#contact"]').textContent = t.navContact;

  // Hero
  document.querySelector('.hero-badge').textContent = t.badge;
  document.querySelector('.hero h1 .highlight').textContent = t.title1;
  document.querySelector('.hero h1').innerHTML = `<span class="highlight">${t.title1}</span><br>${t.title2}`;
  document.querySelector('.hero p').textContent = t.description;
  document.querySelector('.hero-buttons .btn-primary').textContent = t.btnProjects;
  document.querySelector('.hero-buttons .btn-outline').textContent = t.btnMessage;

  // Projects
  document.querySelector('.projects .section-label').textContent = t.sectionProjects;
  document.querySelector('.projects .section-title').textContent = t.projectsTitle;
  document.querySelector('.projects .section-sub').textContent = t.projectsSub;
  const projectCards = document.querySelectorAll('.project-card');
  if (projectCards.length >= 3) {
    projectCards[0].querySelector('h3').textContent = t.project1Title;
    projectCards[0].querySelector('p').textContent = t.project1Desc;
    projectCards[1].querySelector('h3').textContent = t.project2Title;
    projectCards[1].querySelector('p').textContent = t.project2Desc;
    projectCards[2].querySelector('h3').textContent = t.project3Title;
    projectCards[2].querySelector('p').textContent = t.project3Desc;
  }
  document.querySelectorAll('.project-link').forEach(el => el.textContent = t.projectLink);

  // Skills
  document.querySelector('.skills .section-label').textContent = t.sectionSkills;
  document.querySelector('.skills .section-title').textContent = t.skillsTitle;
  document.querySelector('.skills .section-sub').textContent = t.skillsSub;

  // Experience (timeline)
  document.querySelector('.skills-right .section-label').textContent = t.sectionExp;
  document.querySelector('.skills-right .section-title').textContent = t.expTitle;
  document.querySelector('.skills-right .section-sub').textContent = t.expSub;
  const timelineItems = document.querySelectorAll('.timeline-item');
  if (timelineItems.length >= 3) {
    timelineItems[0].querySelector('.timeline-date').textContent = t.exp1Date;
    timelineItems[0].querySelector('.timeline-role').textContent = t.exp1Role;
    timelineItems[0].querySelector('.timeline-company').textContent = t.exp1Company;
    timelineItems[0].querySelector('.timeline-desc').textContent = t.exp1Desc;
    timelineItems[1].querySelector('.timeline-date').textContent = t.exp2Date;
    timelineItems[1].querySelector('.timeline-role').textContent = t.exp2Role;
    timelineItems[1].querySelector('.timeline-company').textContent = t.exp2Company;
    timelineItems[1].querySelector('.timeline-desc').textContent = t.exp2Desc;
    timelineItems[2].querySelector('.timeline-date').textContent = t.exp3Date;
    timelineItems[2].querySelector('.timeline-role').textContent = t.exp3Role;
    timelineItems[2].querySelector('.timeline-company').textContent = t.exp3Company;
    timelineItems[2].querySelector('.timeline-desc').textContent = t.exp3Desc;
  }

  // About
  document.querySelector('.about .section-label').textContent = t.sectionAbout;
  document.querySelector('.about .section-title').textContent = t.aboutTitle;
  const aboutPs = document.querySelectorAll('.about-text p');
  if (aboutPs.length >= 2) {
    aboutPs[0].textContent = t.aboutP1;
    aboutPs[1].textContent = t.aboutP2;
  }
  const aboutLis = document.querySelectorAll('.value-list li');
  if (aboutLis.length >= 4) {
    aboutLis[0].textContent = t.aboutLi1;
    aboutLis[1].textContent = t.aboutLi2;
    aboutLis[2].textContent = t.aboutLi3;
    aboutLis[3].textContent = t.aboutLi4;
  }
  const statCards = document.querySelectorAll('.stat-card');
  if (statCards.length >= 4) {
    statCards[0].querySelector('.stat-number').textContent = t.stat1Number;
    statCards[0].querySelector('.stat-label').textContent = t.stat1Label;
    statCards[1].querySelector('.stat-number').textContent = t.stat2Number;
    statCards[1].querySelector('.stat-label').textContent = t.stat2Label;
    statCards[2].querySelector('.stat-number').textContent = t.stat3Number;
    statCards[2].querySelector('.stat-label').textContent = t.stat3Label;
    statCards[3].querySelector('.stat-number').textContent = t.stat4Number;
    statCards[3].querySelector('.stat-label').textContent = t.stat4Label;
  }

  // Contact
  document.querySelector('.contact .section-label').textContent = t.sectionContact;
  document.querySelector('.contact .section-title').textContent = t.contactTitle;
  document.querySelector('.contact .section-sub').textContent = t.contactSub;
  const contactCards = document.querySelectorAll('.contact-card');
  if (contactCards.length >= 3) {
    contactCards[0].querySelector('.contact-platform').textContent = t.contactTelegram;
    contactCards[1].querySelector('.contact-platform').textContent = t.contactEmail;
    contactCards[2].querySelector('.contact-platform').textContent = t.contactGithub;
  }
  document.querySelector('.contact-cta').textContent = t.contactCta;

  // Footer
  document.querySelector('.footer-copy').textContent = t.footerCopy;
  document.querySelector('.footer-built').innerHTML = t.footerBuilt;

  // Кнопка
  const btn = document.getElementById('lang-toggle');
  btn.textContent = lang === 'ru' ? 'EN' : 'RU';
  btn.classList.toggle('active', lang === 'en');
}

// --- Обработчик клика по кнопке ---

document.addEventListener('DOMContentLoaded', function() {
  const btn = document.getElementById('lang-toggle');
  btn.addEventListener('click', function() {
    const nextLang = currentLang === 'ru' ? 'en' : 'ru';
    switchLanguage(nextLang);
  });

  // По умолчанию — русский (можно заменить на 'en')
  // По умолчанию — английский
switchLanguage('en');
});